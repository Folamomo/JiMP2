#ifndef JIMP_EXERCISES_SUBRACT_H
#define JIMP_EXERCISES_SUBRACT_H

namespace subtract{
    template <typename T> T Subtract(T left, T right){
        return left - right;
    };

    template <typename T> T Add(T left, T right){
        return left + right;
    };
}


#endif //JIMP_EXERCISES_SUBRACT_H
